package Relacionais;

public class Diferente {
	public static void main(String[] args) {
		//Declaração e inicialização das variáveis
		int num1 = 10;
		int num2 = 10;		
		
		//Imprimir o resultado da expressão num1 != num2
		System.out.println(num1 != num2);
		
		//Mudar valor do X
		num1 = 11;
		
		//Imprimir o resultado da expressão x !=y
		System.out.println(num1 != num2);
	}

}
