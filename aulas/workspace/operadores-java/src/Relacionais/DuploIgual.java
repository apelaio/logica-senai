package Relacionais;

public class DuploIgual {
	public static void main(String[] args) {
		//Declaração e inicialização das variáveis
		int x = 4;
		int y =4;
		
		//Imprimir o resultado da expressão x == y
		System.out.println(x == y);
		
		//Mudar valor do X
		x = 3;
		
		//Imprimir o resultado da expressão x ==y
		System.out.println(x == y);
	}

}
